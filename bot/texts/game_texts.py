def update_dice_game_text(original_text, is_win, win_amount):
    """
    –û–±–Ω–æ–≤–ª—è–µ—Ç —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –∏–≥—Ä—ã.

    :param original_text: –ò—Å—Ö–æ–¥–Ω—ã–π —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è.
    :param is_win: –ë—É–ª–µ–≤–æ –∑–Ω–∞—á–µ–Ω–∏–µ (True, –µ—Å–ª–∏ –≤—ã–∏–≥—Ä—ã—à, False, –µ—Å–ª–∏ –ø—Ä–æ–∏–≥—Ä—ã—à).
    :param win_amount: –°—É–º–º–∞ –≤—ã–∏–≥—Ä—ã—à–∞ –∏–ª–∏ –ø—Ä–æ–∏–≥—Ä—ã—à–∞.
    :return: –û–±–Ω–æ–≤–ª–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è.
    """
    # –£–¥–∞–ª—è–µ–º —Å—Ç—Ä–æ–∫—É —Å –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–µ–º –±—Ä–æ—Å–∏—Ç—å –∫—É–±–∏–∫
    updated_text = original_text.split('\n')
    updated_text = [line for line in updated_text if "–ë—Ä–æ—Å—å—Ç–µ" not in line]

    # –î–æ–±–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–µ
